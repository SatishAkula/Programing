
public class Variables {

	public static void main(String args[])
	{
		int int_max_var=2147483647;    //integer variable with max size
		System.out.println(int_max_var);
		
		int int_min_var=-2147483648;
		System.out.println(int_min_var);  //integer variable with min size
		
		byte byte_max_val=127;
		System.out.println(byte_max_val);  //byte variable with max size
		
		byte byte_min_val=-128;
		System.out.println(byte_min_val);  //byte variable with min size
		
		short short_max_val=32767;        //short variable with max size
		System.out.print(short_max_val);
		
		short short_min_val=-32768;      //short variable with min size
		System.out.println(short_min_val);
		
		char char_max_size=65535;          //char variable with max size 
 		System.out.print(char_max_size);
		
 		char char_min_size=0;            //char variable with min size
 		System.out.println(char_min_size);
	}
	
	
}
